
public class EstebanBernalMartinezFutbol {
	public String liga;
	public String equipo;
	public String futbol;
	
	public EstebanBernalMartinezFutbol(String liga, String equipo, String futbol) {
		super();
		this.liga = liga;
		this.equipo = equipo;
		this.futbol = futbol;
	}
	
	public String getLiga() {
		return liga;
	}

	public void setLiga(String liga) {
		this.liga = liga;
	}
	
	public String getEquipo() {
		return equipo;
	}
	public void setEquipo(String equipo) {
		this.equipo = equipo;
	}
	public String getFutbol() {
		return futbol;
	}
}
